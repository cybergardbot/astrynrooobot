def premiumize_lines(text: str, start: int = 0) -> str:
    """Prefix non-empty, non-divider lines with rotating premium emoji."""
    out = []
    idx = start
    for raw in str(text or "").splitlines():
        stripped = raw.strip()
        if not stripped or set(stripped) <= {"─", "━", "-", "—"}:
            out.append(raw)
            continue
        out.append(premium_line(stripped, idx, indent=""))
        idx += 1
    return "\n".join(out)