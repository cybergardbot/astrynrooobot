_BOLD_UPPER = 0x1D5D4
_BOLD_LOWER = 0x1D5EE
_BOLD_DIGIT = 0x1D7EC
def bold(text) -> str:
    """Convert ASCII letters/digits to Unicode sans-serif bold (𝗯𝗼𝗹𝗱).
    Non-ASCII (emoji, accents, symbols) passes through untouched. Apply this
    BEFORE html.escape so escape-generated entities (&amp;) are never bolded.
    Do NOT use inside <code> — bold glyphs break tap-to-copy."""
    out = []
    for ch in str(text or ""):
        o = ord(ch)
        if 0x41 <= o <= 0x5A:
            out.append(chr(_BOLD_UPPER + o - 0x41))
        elif 0x61 <= o <= 0x7A:
            out.append(chr(_BOLD_LOWER + o - 0x61))
        elif 0x30 <= o <= 0x39:
            out.append(chr(_BOLD_DIGIT + o - 0x30))
        else:
            out.append(ch)
    return "".join(out)
