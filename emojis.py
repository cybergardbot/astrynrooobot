_CUSTOM_EMOJI_RE = _re.compile(r'<tg-emoji\s+emoji-id="\d+">([^<]*)</tg-emoji>')
_HTML_TAG_RE = _re.compile(r'(<tg-emoji\s+emoji-id="\d+">[^<]*</tg-emoji>|<[^>]+>)', _re.IGNORECASE)
def _ce(eid: str, fallback: str) -> str:
    """Build a custom emoji HTML tag using the precise emoji-id supplied.
    Uses the caller-provided eid directly so every EMOJI key keeps its
    intended animation.  PREMIUM_EMOJI_IDS is reserved for plain-emoji
    conversion in _premiumize_simple and is never mutated here."""
    return f'<tg-emoji emoji-id="{eid}">{fallback}</tg-emoji>'
def strip_custom_emoji(text: str) -> str:
    """Remove Telegram custom emoji tags for fallback paths without plain emoji."""
    return _CUSTOM_EMOJI_RE.sub("", str(text or ""))
def is_emoji_send_error(err_text: str) -> bool:
    """Return True if a Telegram error is caused by premium custom emoji tags."""
    err = str(err_text or "").lower()
    return any(p in err for p in (
        "document_invalid",
        "document invalid",
        "wrong custom emoji",
        "custom emoji identifier",
        "invalid custom emoji",
    ))
_TOKEN_RE = _re.compile(r'(<[^>]+>|&[a-zA-Z][a-zA-Z0-9]*;|&#\d+;)')
def boldify_b_tags(text: str) -> str:
    """Render <b>/<strong> content as Unicode sans-serif bold, then drop the
    tags. Content inside <code>/<pre> is never bolded (stays tap-to-copy) and
    HTML entities (&amp; etc.) are preserved intact."""
    text = str(text or "")
    if "<b>" not in text and "<strong>" not in text:
        return text
    out = []
    skip_depth = 0
    b_depth = 0
    pos = 0
    for m in _TOKEN_RE.finditer(text):
        if m.start() > pos:
            chunk = text[pos:m.start()]
            out.append(bold(chunk) if (b_depth and not skip_depth) else chunk)
        tok = m.group(0)
        low = tok.lower()
        if low in ("<b>", "<strong>"):
            b_depth += 1
        elif low in ("</b>", "</strong>"):
            b_depth = max(0, b_depth - 1)
        elif low.startswith("<code") or low.startswith("<pre"):
            skip_depth += 1
            out.append(tok)
        elif low.startswith("</code") or low.startswith("</pre"):
            skip_depth = max(0, skip_depth - 1)
            out.append(tok)
        else:
            out.append(tok)
        pos = m.end()
    if pos < len(text):
        chunk = text[pos:]
        out.append(bold(chunk) if (b_depth and not skip_depth) else chunk)
    return "".join(out)
def _premiumize_simple(text: str) -> str:
    """Replace plain emoji with premium <tg-emoji> tags using placeholder approach."""
    if not text:
        return text
    placeholders = []
    result = text
    for i, (emoji, doc_id) in enumerate(sorted(PREMIUM_EMOJI_IDS.items(), key=lambda item: len(item[0]), reverse=True)):
        placeholder = f"\x00PE{i:02d}\x00"
        placeholders.append((placeholder, doc_id, emoji))
        result = result.replace(emoji, placeholder)
    for placeholder, doc_id, emoji in placeholders:
        result = result.replace(placeholder, f'<tg-emoji emoji-id="{doc_id}">{emoji}</tg-emoji>')
    return result
def premiumize_emoji_html(text: str) -> str:
    """Convert plain emoji in HTML bot text to Telegram premium custom emoji tags.
    Uses simple placeholder replacement (like commands/bot.py).
    Existing <tg-emoji> tags and <code>/<pre> content are left untouched so card
    lines, IDs, and debug blobs stay copy-friendly.
    """
    text = str(text or "")
    if not text:
        return text
    out: list[str] = []
    skip_depth = 0
    pos = 0
    for match in _HTML_TAG_RE.finditer(text):
        if match.start() > pos:
            chunk = text[pos:match.start()]
            if skip_depth:
                out.append(chunk)
            else:
                out.append(_premiumize_simple(chunk))
        tag = match.group(0)
        out.append(tag)
        tag_lower = tag.lower()
        if tag_lower.startswith("<code") or tag_lower.startswith("<pre"):
            skip_depth += 1
        elif tag_lower.startswith("</code") or tag_lower.startswith("</pre"):
            skip_depth = max(0, skip_depth - 1)
        pos = match.end()
    if pos < len(text):
        chunk = text[pos:]
        if skip_depth:
            out.append(chunk)
        else:
            out.append(_premiumize_simple(chunk))
    return "".join(out)
